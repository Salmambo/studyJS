//배수 판별기
//엘리스 토끼는 자연수가 입력될 때 입력된 숫자가 2, 3, 5, 7중 어떤 수의 배수인지 알려주는 프로그램을 만들려고 합니다.
//지시사항을 참고하여 코드를 작성하세요.
/* 지시사항
    1. num 매개변수로부터 자연수 하나를 입력받고 2, 3, 5, 7 중 어떤 수의 배수인지 아래 표를 참고하여 결과를 출력하세요.
        (여러 수의 배수라면 여러 줄에 걸쳐 출력하세요.)
    2. answer 배열에 출력문을 쌓아 join 메소드를 활용해 줄바꿈을 넣어 출력해보세요.
*/
/* 예시
    입력    출력
    9       3의 배수입니다.
    10      2의 배수입니다.
            5의 배수입니다.
*/
function solution(num) {
    var answer = [];
    division(num, 2, answer);
    division(num, 3, answer);
    division(num, 5, answer);
    division(num, 7, answer);
    return answer.join("\n");
}
function division(num, divisor, answer) {
    let multiple = false;
    while (num % divisor == 0) {
        num = parseInt(num / divisor);
        multiple = true;
    }
    if (multiple) {
        answer.push(divisor + "의 배수입니다.");
    }
}

